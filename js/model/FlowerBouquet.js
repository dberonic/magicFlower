export class FlowerBouquet {

    /**
     * Constructor for flower bouquet object
     * @param {String} paper 
     * @param {String} ribon 
     * @param {String} flowers 
     * @param {String} image 
     */
    constructor(paper, ribon, flowers, image) {

        this._paper = paper;
        this._ribon = ribon;
        this._flowers = flowers;
        this._image = image; // only final image goes here 
    }

}